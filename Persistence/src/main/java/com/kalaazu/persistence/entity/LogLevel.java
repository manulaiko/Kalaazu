/**
 * Server log level.
 * =================
 *
 *  * 0 = emergency
 *  * 1 = alert
 *  * 2 = critical
 *  * 3 = error
 *  * 4 = warning
 *  * 5 = notice
 *  * 6 = info
 *  * 7 = debug
 *
 * @author Manulaiko <manulaiko@gmail.com>
 */
public enum LogLevel {
    EMERGENCY,
    ALERT,
    CRITICAL,
    ERROR,
    WARNING,
    NOTICE,
    INFO,
    DEBUG
}
